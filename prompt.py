# -------------------------------
# Prompt 선택
# -------------------------------

PROMPT_MAP = {

    "시장": MARKET_PROMPT,

    "우리금융": WOORI_PROMPT,

    "운영": OPERATION_PROMPT,

    "경쟁": COMPETITOR_PROMPT,

    "기본": GENERAL_PROMPT

}



# -------------------------------
# Prompt 반환
# -------------------------------

def get_prompt(prompt_type="기본"):

    return PROMPT_MAP.get(

        prompt_type,

        GENERAL_PROMPT

    )

# -------------------------------
# 질문별 Prompt 자동 선택
# -------------------------------

def detect_prompt_type(question):

    q = question.replace(" ", "")

    if any(
        x in q
        for x in [
            "시장",
            "시장전망",
            "시장분석",
            "시장현황",
            "시장동향",
            "시장분위기",
            "시장흐름"
        ]
    ):
        return "시장"

    elif any(
        x in q
        for x in [
            "우리금융",
            "우리금융저축은행",
            "우리금융전략",
            "우리금융현황"
        ]
    ):
        return "우리금융"

    elif any(
        x in q
        for x in [
            "운영",
            "운영방향",
            "금리운영",
            "조달",
            "ALM",
            "수신운영",
            "운영전략"
        ]
    ):
        return "운영"

    elif any(
        x in q
        for x in [
            "경쟁",
            "비교",
            "벤치마킹",
            "타사",
            "경쟁사",
            "경쟁은행",
            "시장비교"
        ]
    ):
        return "경쟁"

    return "기본"